#include <unistd.h>
#include "libft.h"

void	ft_putendl(char const *s)
{
	char c;

	if (s == NULL)
		return ;
	while (*s)
	{
		c = *s;
		write(1, &c, 1);
		s++;
	}
	write(1, "\n", 1);
}
